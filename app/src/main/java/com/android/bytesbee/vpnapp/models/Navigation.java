package com.android.bytesbee.vpnapp.models;

public class Navigation {
    int icon;
    String title;

    public Navigation(String title, int icon) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
